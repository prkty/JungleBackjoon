# 물론 이 아스키 코드를 매칭시켜서 도서관처럼 입력된 데이터 중 아키코드 값을 불러오는 형식을 쓸수도 있다.
# 그러면 굉장히 데이터 양도 많아지고 코딩하기 쉽지 않다.
# 파이썬에서는 ord() 함수를 쓰면 아스키 코드를 추출할 수 있다.
i = input()
ASCIICODE = ord(i)
print(ASCIICODE)

