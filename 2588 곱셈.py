# 이 문제를 풀기 위해서는 A를 정수로 처리하고 B를 문자열 형태로 취급해서 각 자리를 뽑아낸 다음 각 자리 수를 정수형으로 변환해 곱해야된다는 사실이다. 
# B를 문자열로 해서 일의 자리 숫자, 십의 자리 숫자 등 나누어서 각각 곱하면 된다.
A = int(input())
B = input()
print(A*int(B[2]))  # 문자형이 었기 때문에 자연수로 바꿀 int정수 자료형으로 변환해야한다. 또한 맨 오른쪽 수부터 계산해야하므로 리스트 2부터 시작이다.
print(A*int(B[1]))  # 참고로 리스트에서 첫번째 자리는 0으로 호출한다.
print(A*int(B[0]))
print(A*int(B))


