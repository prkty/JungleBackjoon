# 문제의 주요 점은 테스트 케이스 T가 첫번째 줄에 온다는 것이다. 그러고 테스트 케이스만큼 A+B를 수행한다.
T = int(input())
for i in range(T):
    a, b = map(int, input().split())
    print(a+b)
# 이거는 T라는 테스트 케이스를 설정하고 그만큼 수를 받고 결과를 표현하는 것을 어떻게 하는지 몰라 헤멧다.
# for range에서 range를 T로 설정하고 입력을 받으면 테스트 케이스 횟수를 지정 가능하다. 그러고 입력 받을 식을 쓰면 된다.
